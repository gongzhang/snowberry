package fatcat.gui.util;

/**
 * 迭代器提供了遍历<code>LinkedList</code>链表类的接口。
 * @author 张弓
 * @version 1.01
 */
public interface Iterator {

	/**
	 * 判断迭代器中是否存在下一个元素。
	 * @return 是否存在下一个元素
	 */
	public boolean hasNext();

	/**
	 * 取得下一个元素的引用。如不存在则返回<code>null</code>。
	 * @return 下一个元素的引用
	 */
	public Object next();
}
