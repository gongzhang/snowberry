package fatcat.snowberry.diagram;

import fatcat.gui.snail.Component;
import fatcat.gui.snail.Container;


public class CompositeItemGhost extends Component {
	
	private final CompositeItem item;

	public CompositeItemGhost(Container owner, CompositeItem item) {
		super(owner);
		this.item = item;
		setSkin(item.getSkin());
	}
	
	public CompositeItem getItem() {
		return item;
	}

}
